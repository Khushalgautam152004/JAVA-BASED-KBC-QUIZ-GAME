private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    
    String uname=jTextField1.getText();
    String email=jTextField2.getText();
    String password=jPasswordField1.getText();
    
    String gender="";
    if(jRadioButton1.isSelected())
    {
        gender="Male";
    }
    if(jRadioButton2.isSelected())
    {
        gender="Female";
    }
    
    String city=(String) jComboBox1.getSelectedItem();
    
    if(uname.equals("") || uname==null)
    {
        JOptionPane.showMessageDialog(this, "Name cannot be null");
    }
    else if(uname.length()<3 || uname.length()>25)
    {
        JOptionPane.showMessageDialog(this, "Name should be of length 3-25 characters");
    }
    else if(email.equals("") || email==null)
    {
        JOptionPane.showMessageDialog(this, "Email cannot be null");
    }
    else if(password.equals("") || password==null)
    {
        JOptionPane.showMessageDialog(this, "Password cannot be null");
    }
    else if(gender.equals("") || gender==null)
    {
        JOptionPane.showMessageDialog(this, "Gender cannot be null");
    }
    else if(city.equals("") || city==null)
    {
        JOptionPane.showMessageDialog(this, "City cannot be null");
    }
    else
    {
        try
        {
            Connection con=DBConnection.connect();
            PreparedStatement ps=con.prepareStatement("insert into KBC_REGISTRATION values (?,?,?,?,?)");
            ps.setString(1, uname);
            ps.setString(2, email);
            ps.setString(3, password);
            ps.setString(4, gender);
            ps.setString(5, city);
            
            ps.executeUpdate();
            con.close();

            JOptionPane.showMessageDialog(rootPane, "Your Account Has Successfully Created");
            UserPanel up=new UserPanel(email);
            up.setVisible(true);
            setVisible(false);
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
    
    
    
}//GEN-LAST:event_jButton1ActionPerformed

